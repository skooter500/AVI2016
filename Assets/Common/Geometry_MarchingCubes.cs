	private static int CalculateCubeIndex(float[] values)
	{
		int cubeIndex = 0;
		for (int i = 0; i < values.Length; i++)
		{
			if (values[i] > 0)
			{
				cubeIndex |= 1 << i;
			}
		}
		return cubeIndex;
	}

	private static Vector3[] CalculateVertices(Vector3[] points, float[] values, int cubeIndex)
	{
		var vertList = new Vector3[12]; // array of points of result surface

		// Cube is entirely in/out of the surface
		if (edgeTable[cubeIndex] == 0)
		{
			return null;
		}

		// Find the vertices where the surface intersects the cube
		if ((edgeTable[cubeIndex] & 1) != 0)
		{
			vertList[0] = Lerp(points[0], points[1], values[0], values[1]);
		}
		if ((edgeTable[cubeIndex] & 2) != 0)
		{
			vertList[1] = Lerp(points[1], points[2], values[1], values[2]);
		}
		if ((edgeTable[cubeIndex] & 4) != 0)
		{
			vertList[2] = Lerp(points[2], points[3], values[2], values[3]);
		}
		if ((edgeTable[cubeIndex] & 8) != 0)
		{
			vertList[3] = Lerp(points[3], points[0], values[3], values[0]);
		}
		if ((edgeTable[cubeIndex] & 16) != 0)
		{
			vertList[4] = Lerp(points[4], points[5], values[4], values[5]);
		}
		if ((edgeTable[cubeIndex] & 32) != 0)
		{
			vertList[5] = Lerp(points[5], points[6], values[5], values[6]);
		}
		if ((edgeTable[cubeIndex] & 64) != 0)
		{
			vertList[6] = Lerp(points[6], points[7], values[6], values[7]);
		}
		if ((edgeTable[cubeIndex] & 128) != 0)
		{
			vertList[7] = Lerp(points[7], points[4], values[7], values[4]);
		}
		if ((edgeTable[cubeIndex] & 256) != 0)
		{
			vertList[8] = Lerp(points[0], points[4], values[0], values[4]);
		}
		if ((edgeTable[cubeIndex] & 512) != 0)
		{
			vertList[9] = Lerp(points[1], points[5], values[1], values[5]);
		}
		if ((edgeTable[cubeIndex] & 1024) != 0)
		{
			vertList[10] = Lerp(points[2], points[6], values[2], values[6]);
		}
		if ((edgeTable[cubeIndex] & 2048) != 0)
		{
			vertList[11] = Lerp(points[3], points[7], values[3], values[7]);
		}

		return vertList;
	}

	private static Vector3 Lerp(Vector3 p1, Vector3 p2, float v1, float v2)
	{
		var p = p1 + (0 - v1) * (p2 - p1) / (v2 - v1);
		return p;
	}

	private void AppendTriangles(int cubeIndex, Vector3[] vertList, Func<Vector3, float> scalarField)
	{
		for (int i = 0; triTable[cubeIndex, i] != -1; i += 3)
		{
			int vertexCount = vertices.Count;

			var v0 = vertList[triTable[cubeIndex, i + 0]];
			var v1 = vertList[triTable[cubeIndex, i + 1]];
			var v2 = vertList[triTable[cubeIndex, i + 2]];

			vertices.Add(v0);
			vertices.Add(v1);
			vertices.Add(v2);

			indices.Add(vertexCount + 0);
			indices.Add(vertexCount + 1);
			indices.Add(vertexCount + 2);

			var n0 = CalculateGradient(v0, scalarField).normalized;
			var n1 = CalculateGradient(v1, scalarField).normalized;
			var n2 = CalculateGradient(v2, scalarField).normalized;

			normals.Add(n0);
			normals.Add(n1);
			normals.Add(n2);

			var direction = (n0 + n1 + n2).ToDirection();
			switch (direction)
			{
				case Direction.Up:
					uvs.Add(new Vector2(v0.x, v0.z));
					uvs.Add(new Vector2(v1.x, v1.z));
					uvs.Add(new Vector2(v2.x, v2.z));
					break;

				case Direction.Down:
					uvs.Add(new Vector2(v0.x, -v0.z));
					uvs.Add(new Vector2(v1.x, -v1.z));
					uvs.Add(new Vector2(v2.x, -v2.z));
					break;

				case Direction.North:
					uvs.Add(new Vector2(v0.x, v0.y));
					uvs.Add(new Vector2(v1.x, v1.y));
					uvs.Add(new Vector2(v2.x, v2.y));
					break;

				case Direction.South:
					uvs.Add(new Vector2(-v0.x, v0.y));
					uvs.Add(new Vector2(-v1.x, v1.y));
					uvs.Add(new Vector2(-v2.x, v2.y));
					break;

				case Direction.East:
					uvs.Add(new Vector2(v0.z, v0.y));
					uvs.Add(new Vector2(v1.z, v1.y));
					uvs.Add(new Vector2(v2.z, v2.y));
					break;

				case Direction.West:
					uvs.Add(new Vector2(-v0.z, v0.y));
					uvs.Add(new Vector2(-v1.z, v1.y));
					uvs.Add(new Vector2(-v2.z, v2.y));
					break;

				default:
					throw new ArgumentOutOfRangeException();
			}
		}
	}

	private const float DELTA = .01f;
	private static readonly Vector3 deltaX = new Vector3(DELTA, 0, 0);
	private static readonly Vector3 deltaY = new Vector3(0, DELTA, 0);
	private static readonly Vector3 deltaZ = new Vector3(0, 0, DELTA);

	const int SHIFT = 7;
	const int ARRAY_SIZE = 1 << (3 * SHIFT);
	const int NEXT_X = 1 << (2 * SHIFT);
	const int NEXT_Y = 1 << SHIFT;
	const int NEXT_Z = 1;
	private readonly float[] values = new float[ARRAY_SIZE];
	private readonly Vector3[] points = new Vector3[8];
	private readonly float[] localValues = new float[8];

	private static Vector3 CalculateGradient(Vector3 position, Func<Vector3, float> scalarField)
	{
		float value = scalarField(position);
		float dx = (scalarField(position + deltaX) - value) / DELTA;
		float dy = (scalarField(position + deltaY) - value) / DELTA;
		float dz = (scalarField(position + deltaZ) - value) / DELTA;

		return new Vector3(dx, dy, dz);
	}

