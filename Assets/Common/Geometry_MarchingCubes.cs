	public void AppendIsosurface(Func<Vector3, float> scalarField, Bounds bounds, int samplingResolution)
	{
		if (samplingResolution > 127)
		{
			throw new ArgumentOutOfRangeException("Resolution is higher than 127.");
		}

		float x0 = bounds.min.x;
		float y0 = bounds.min.y;
		float z0 = bounds.min.z;
		float dx = bounds.size.x / samplingResolution;
		float dy = bounds.size.y / samplingResolution;
		float dz = bounds.size.z / samplingResolution;

		float wx = x0;
		for (int x = 0; x <= samplingResolution; x++, wx += dx)
		{
			float wy = y0;
			for (int y = 0; y <= samplingResolution; y++, wy += dy)
			{
				float wz = z0;
				for (int z = 0; z <= samplingResolution; z++, wz += dz)
				{
					int pointer = (((x << SHIFT) | y) << SHIFT) | z;

					float value = scalarField(new Vector3(wx, wy, wz));
					values[pointer] = value;
				}
			}
		}

		wx = x0;
		for (int x = 0; x < samplingResolution; x++, wx += dx)
		{
			float wy = y0;
			for (int y = 0; y < samplingResolution; y++, wy += dy)
			{
				float wz = z0;
				for (int z = 0; z < samplingResolution; z++, wz += dz)
				{
					int pointer = (((x << SHIFT) | y) << SHIFT) | z;

					points[0] = new Vector3(wx, wy, wz + dz);
					points[1] = new Vector3(wx + dx, wy, wz + dz);
					points[2] = new Vector3(wx + dx, wy, wz);
					points[3] = new Vector3(wx, wy, wz);
					points[4] = new Vector3(wx, wy + dy, wz + dz);
					points[5] = new Vector3(wx + dx, wy + dy, wz + dz);
					points[6] = new Vector3(wx + dx, wy + dy, wz);
					points[7] = new Vector3(wx, wy + dy, wz);

					localValues[0] = values[pointer + NEXT_Z];
					localValues[1] = values[pointer + NEXT_X + NEXT_Z];
					localValues[2] = values[pointer + NEXT_X];
					localValues[3] = values[pointer];
					localValues[4] = values[pointer + NEXT_Y + NEXT_Z];
					localValues[5] = values[pointer + NEXT_Y + NEXT_X + NEXT_Z];
					localValues[6] = values[pointer + NEXT_Y + NEXT_X];
					localValues[7] = values[pointer + NEXT_Y];

					int cubeIndex = CalculateCubeIndex(localValues);
					var vertList = CalculateVertices(points, localValues, cubeIndex);

					AppendTriangles(cubeIndex, vertList, scalarField);
				}
			}
		}
	}

